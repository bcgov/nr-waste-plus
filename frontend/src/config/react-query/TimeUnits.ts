/**
 * Shared time constants used by React Query and other polling or cache settings.
 * All values are expressed in milliseconds.
 */
export const ONE_SECOND = 1000;

/**
 * Three-second duration in milliseconds.
 */
export const THREE_SECONDS = 3 * ONE_SECOND;

/**
 * Five-second duration in milliseconds.
 */
export const FIVE_SECONDS = 5 * ONE_SECOND;

/**
 * Eight-second duration in milliseconds.
 */
export const EIGHT_SECONDS = 8 * ONE_SECOND;

/**
 * Ten-second duration in milliseconds.
 */
export const TEN_SECONDS = 10 * ONE_SECOND;

/**
 * Thirty-second duration in milliseconds.
 */
export const THIRTY_SECONDS = 30 * ONE_SECOND;

/**
 * One-minute duration in milliseconds.
 */
export const ONE_MINUTE = 60 * ONE_SECOND;

/**
 * Two-minute duration in milliseconds.
 */
export const TWO_MINUTE = 2 * ONE_MINUTE;

/**
 * One-hour duration in milliseconds.
 */
export const ONE_HOUR = 60 * ONE_MINUTE;

/**
 * Three-hour duration in milliseconds.
 */
export const THREE_HOURS = 3 * ONE_HOUR;

/**
 * Three-and-a-half-hour duration in milliseconds.
 */
export const THREE_HALF_HOURS = 3.5 * ONE_HOUR;
